package com.vabono;

public class Anio {

	public int anio;
	public int total;
	public int porcentaje;
	
	public Anio(int anio, int total, int porcentaje) {
		super();
		this.anio = anio;
		this.total = total;
		this.porcentaje = porcentaje;
	}
	
	public int getAnio() {
		return anio;
	}
	public void setAnio(int anio) {
		this.anio = anio;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getPorcentaje() {
		return porcentaje;
	}
	public void setPorcentaje(int porcentaje) {
		this.porcentaje = porcentaje;
	}
	
	
}
